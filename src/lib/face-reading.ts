/** 선택값 문자열을 기반으로 간단한 해시를 생성한다 */
function simpleHash(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash |= 0; // 32비트 정수 변환
  }
  return Math.abs(hash);
}

// ─── 종합 분석 함수 ─────────────────────────────────────────

/**
 * 사용자의 관상 선택을 분석하여 종합 결과를 반환한다.
 * @param selections - { categoryId: optionId } 매핑
 */
export function analyzeFaceReading(
  selections: Record<string, string>,
): FaceReadingResult {
  const interpretations: Record<string, FaceReadingInterpretation> = {};

  // 각 카테고리별 해석 조회
  for (const [categoryId, optionId] of Object.entries(selections)) {
    const categoryInterps = INTERPRETATIONS[categoryId];
    if (categoryInterps && categoryInterps[optionId]) {
      interpretations[categoryId] = categoryInterps[optionId];
    }
  }

  const entries = Object.values(interpretations);
  if (entries.length === 0) {
    return {
      selections,
      interpretations,
      overall: {
        personality: '선택된 관상 정보가 없습니다.',
        fortune: '선택된 관상 정보가 없습니다.',
        love: '선택된 관상 정보가 없습니다.',
        career: '선택된 관상 정보가 없습니다.',
        luckyColor: '금색',
        luckyNumber: 7,
        overallScore: 50,
      },
    };
  }

  // 전체 점수 계산 (각 해석의 rating 평균 × 20)
  const avgRating =
    entries.reduce((sum, e) => sum + e.rating, 0) / entries.length;
  const overallScore = Math.round(avgRating * 20);

  // 해시 기반 행운 색상/숫자
  const selectionStr = Object.entries(selections)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([k, v]) => `${k}:${v}`)
    .join('|');
  const hash = simpleHash(selectionStr);
  const luckyColor = LUCKY_COLORS[hash % LUCKY_COLORS.length];
  const luckyNumber = (hash % 9) + 1; // 1~9

  // 높은 rating의 해석에서 우선적으로 종합 문구를 추출
  const sorted = [...entries].sort((a, b) => b.rating - a.rating);

  // 성격 종합: 상위 3개 특성 조합
  const topPersonality = sorted.slice(0, 3);
  const personalityTraits = topPersonality.map((e) => {
    const firstSentence = e.personality.split('.')[0];
    return firstSentence;
  });

  // 재물 종합
  const topFortune = sorted.slice(0, 3);
  const fortuneTraits = topFortune.map((e) => {
    const firstSentence = e.fortune.split('.')[0];
    return firstSentence;
  });

  // 연애 종합
  const topLove = sorted.slice(0, 3);
  const loveTraits = topLove.map((e) => {
    const firstSentence = e.love.split('.')[0];
    return firstSentence;
  });

  // 직업 종합
  const topCareer = sorted.slice(0, 3);
  const careerTraits = topCareer.map((e) => {
    const firstSentence = e.career.split('.')[0];
    return firstSentence;
  });

  // 종합 운세 등급 결정
  const gradePrefix =
    overallScore >= 90
      ? '대길(大吉)! '
      : overallScore >= 75
        ? '길(吉)! '
        : overallScore >= 60
          ? '중길(中吉). '
          : overallScore >= 40
            ? '소길(小吉). '
            : '평(平). ';

  return {
    selections,
    interpretations,
    overall: {
      personality: `${gradePrefix}${personalityTraits.join('. ')}. 이러한 특성들이 조화를 이루어 균형 잡힌 성격을 만들어냅니다.`,
      fortune: `${gradePrefix}${fortuneTraits.join('. ')}. 전반적으로 ${overallScore >= 70 ? '재물운이 좋은 편이니, 적극적으로 기회를 잡으세요.' : '안정적인 재정 관리가 중요합니다. 무리한 투자보다는 꾸준한 저축이 현명합니다.'}`,
      love: `${gradePrefix}${loveTraits.join('. ')}. ${overallScore >= 70 ? '좋은 인연을 만날 가능성이 높으며, 진심을 다하면 행복한 관계를 이룰 수 있습니다.' : '서로를 이해하고 배려하는 마음이 행복한 연애의 열쇠입니다.'}`,
      career: `${gradePrefix}${careerTraits.join('. ')}. ${overallScore >= 70 ? '재능을 살려 도전하면 큰 성과를 거둘 수 있는 상입니다.' : '꾸준한 노력과 전문성 향상이 성공의 지름길입니다.'}`,
      luckyColor,
      luckyNumber,
      overallScore,
    },
  };
}
